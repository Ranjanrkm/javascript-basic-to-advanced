/**
 * JavaScript String Methods:
 * toLowerCase() and toUpperCase()
 * Beginner-friendly and fully commented
 */

// Create a string
const message = "I am Ranjan Kumar Mahato and I am 111 years old!!";

// ----------------------------------------------------
// toLowerCase()
// ----------------------------------------------------
// Converts all letters in the string to lowercase
console.log(message.toLowerCase());
// Output: i am ranjan kumar mahato and i am 111 years old!!

// The original string does not change
console.log(message);
// Output: I am Ranjan Kumar Mahato and I am 111 years old!!

// ----------------------------------------------------
// toUpperCase()
// ----------------------------------------------------
// Converts all letters in the string to uppercase
console.log(message.toUpperCase());
// Output: I AM RANJAN KUMAR MAHATO AND I AM 111 YEARS OLD!!